package modelo;

public class Usuario {

	int codigoUsuario;
	String nome;
	String email;
	int numeroValidacao;
	String statusValidacao;
	String apelido;
	String senha;
	String perfil;
	
	public int getCodigoUsuario() {
		return codigoUsuario;
	}
	public void setCodigoUsuario(int codigoUsuario) {
		this.codigoUsuario = codigoUsuario;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getNumeroValidacao() {
		return numeroValidacao;
	}
	public void setNumeroValidacao(int numeroValidacao) {
		this.numeroValidacao = numeroValidacao;
	}
	public String getStatusValidacao() {
		return statusValidacao;
	}
	public void setStatusValidacao(String statusValidacao) {
		this.statusValidacao = statusValidacao;
	}
	public String getApelido() {
		return apelido;
	}
	public void setApelido(String apelido) {
		this.apelido = apelido;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getPerfil() {
		return perfil;
	}
	public void setPerfil(String perfil) {
		this.perfil = perfil;
	}
	
	
	
	
	
}
