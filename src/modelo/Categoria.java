package modelo;

public class Categoria {

	int codigoCategoria;
	int nivelCategoria;
	String descricaoCategoria;
	int ordemExibicao;
	int codigoCategoriaPai;
	int nivelCategoriaPai;
	
	public int getCodigoCategoria() {
		return codigoCategoria;
	}
	public void setCodigoCategoria(int codigoCategoria) {
		this.codigoCategoria = codigoCategoria;
	}
	public int getNivelCategoria() {
		return nivelCategoria;
	}
	public void setNivelCategoria(int nivelCategoria) {
		this.nivelCategoria = nivelCategoria;
	}
	public String getDescricaoCategoria() {
		return descricaoCategoria;
	}
	public void setDescricaoCategoria(String descricaoCategoria) {
		this.descricaoCategoria = descricaoCategoria;
	}
	public int getOrdemExibicao() {
		return ordemExibicao;
	}
	public void setOrdemExibicao(int ordemExibicao) {
		this.ordemExibicao = ordemExibicao;
	}
	public int getCodigoCategoriaPai() {
		return codigoCategoriaPai;
	}
	public void setCodigoCategoriaPai(int codigoCategoriaPai) {
		this.codigoCategoriaPai = codigoCategoriaPai;
	}
	public int getNivelCategoriaPai() {
		return nivelCategoriaPai;
	}
	public void setNivelCategoriaPai(int nivelCategoriaPai) {
		this.nivelCategoriaPai = nivelCategoriaPai;
	}
	
	
	
}
